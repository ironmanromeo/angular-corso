class Datastore {
  private _dati :string = ""

  get dati() {
    console.log("sto leggendo i dati");

    return this._dati
  }

  set dati(dati :string) {
    if (dati !== "pippo") {
      console.log("sto settando i dati");
      this._dati = dati
    } else {
      console.log("Errore!");

    }

  }

}

const ds = new Datastore()

ds.dati = "pippo"

console.log(ds.dati);

